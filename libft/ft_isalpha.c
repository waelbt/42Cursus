#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned int	b;

	if (c >= 65 && c <= 90)
		return (b = 1);
	else if (c >= 97 && c <= 122)
		return (b = 2);
	else
		return (b = 0);
}
